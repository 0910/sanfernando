							<h4>LA IGLESIA MÁS ANTIGUA</h4>
							<p>Bajo la advocación de Nuestra Señora del Rosario (patrona del Delta del Paraná), la Parroquia del Salvador es la iglesia mas antigua del territorio isleño.
Inaugurada el 25 de mayo de 1908, se encuentra en la intersección de los ríos Paraná Minì y Chaná, en el solar que vecinos del lugar donaron en 1905, con el fin de establecer allí un espacio para la celebración del culto católico.
La construcción de la Capilla –traída de Europa- fue iniciada por el sacerdote Juan Delleye, y concluida por el cura párroco Maximino Pérez.</p>
							<h4>CLUB MOTONAUTICO ARGENTINO Y DE REGATAS INDEPENDENCIA</h4>
							<p>Fundado el 9 de Julio del año 1920, un grupo de vecinos del Delta, lo bautizò bajo el nombre de <strong>Independencia</strong>. Los mismos, en un modesto pero simbólico mástil realizado en sauce, izaron la Bandera Argentina y entonaron las estrofas del Himno Nacional, a orillas del río Paraná Miní. Aquella junta organizadora estaba formada por Ricardo Sarthou, Felipe y Antonio Giorgio y Francisco Lissarrague.
Al poco tiempo construyeron la sede en el Paraná Miní y Arroyo Chaná y el 31 de octubre de 1936 realizaron el Primer Congreso Isleño donde, como se señala más arriba, se estableciò que el 31 de octubre se conmemore el Día de los Isleños.
 
Desde entonces, el club continuó siendo centro de reuniones sociales y deportivas, bailes, festivales, regatas de remo y de motonáutica. En 1978 se fusionó con otro club y pasó a llamarse Motonáutico Argentino y de Regatas Independencia, formando una sola institución deportiva al servicio de la comunidad sanfernandina.</p>
