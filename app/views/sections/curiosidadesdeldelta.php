							<h4>HISTORIA</h4>	
							<p>El 18 de febrero de 1938, a las islas arribò un pasajero con la pulcritud típica de los intelectuales de entonces. El hombre se encontraba refugiado detrás de un par de lentes enormes que le otorgaban un aspecto exótico. Mientras preparaba  su cena, él decidió dar una vuelta por los alrededores. Al regresar pidió al dependiente Luis Giúdici, hijo de la familia propietaria, un vaso de whisky y un poco de agua, a la vez que sostenìa en su mano un frasquito de medicamentos. Luego, se alojò en la habitación N° 9, se encerrò y ocurrió una tragedia
Luis llamó a los huéspedes para cenar y sólo faltaba el señor de traje claro y anteojos redondos. Fue en su búsqueda y al no encontrar respuesta, forzó la puerta. Vestido, sobre la cama, con la cabeza hacia abajo, yacía el cuerpo del insólito personaje. El contenido del frasquito era cianuro, por lo que, el ocupante de la habitación, se había suicidado. A la mañana siguiente, absorto y sin explicaciones, el mundo intelectual más significativo de Buenos Aires, lloraba la muerte de don Leopoldo Lugones. El grupo “Amistad 25” en recuerdo de lo ocurrido colocó una placa en la habitación N° 9.</p>	
