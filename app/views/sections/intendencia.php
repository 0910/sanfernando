						<p>El intendente Luis Andreotti asumió su mandato el 10 de diciembre de 2011. Las prioridades de la gestión que lleva adelante junto a los miembros del Departamento Ejecutivo, se resumen en los siguientes puntos principales:</p>
						 <ul>
						 	<li class="bullet"><p>La Seguridad y las estrategias a implementar con fondos municipales (cámaras de seguridad, patrullas y destacamentos)</p></li>
						 	<li class="bullet"><p>La Cultura y el Deporte, fomentando competencias y espectáculos regionales como herramienta de contención social (Escuelas de Formación Artística y Boxeo)</p></li>
						 	<li class="bullet"><p>La Salud y el trabajo de campo realizado desde el área de Desarrollo Social, Medio Ambiente y Salud Pública, para disminuir el alto índice de mortalidad infantil en el distrito.</p></li>
						 	<li class="bullet"><p>La Economía y la importancia de la buena administración de los recursos de los vecinos.</p></li>
						 	<li class="bullet"><p>La Educación, como motor que permite igualar oportunidades.</p></li>
						 	<li class="bullet"><p>Obras Públicas, con el fin de modernizar los espacios urbanos de la ciudad (IUNA, Túnel 202, Túnel Simón de Iriondo, Plaza Dorrego, etc)</p></li>
						 	<li class="bullet"><p>La relación con las empresas prestadoras de servicios: TBA, Tren de la Costa, Autopistas del Sol, Transporte automotor de pasajeros, AySA S.A, Edenor y Cables Antenas.</p></li>
						 	<li class="bullet"><p>La relación Provincia- Municipio en los temas de: coparticipación, créditos con Banco Provincia, impuestos y descentralización e ingresos por juegos de azar.</p></li>
						 	<li class="bullet"><p>La municipalización del servicio de barrido de las calles y el reforzamiento de otros servicios de limpieza.</p></li>
						 </ul>
